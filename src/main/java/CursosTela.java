private void buscarCursos() {
    try {
        CursoDAO cd = new CursoDAO();
        Curso[] cursos = cd.obterCursos();
        cursosComboBox.setModel(
                new DefaultComboBoxModel<>(cursos));
    }
    catch (Exception e) {
        JOptionPane.showMessageDialog(
        null, 
        "não encontrei lista de cursos");
        e.printStackTrace();
    }
}


